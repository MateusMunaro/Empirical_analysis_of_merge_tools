public  class  Staff {
	
    private int staffId;

	
    private String name;

	

    public Staff(int staffId, String name) {
        this.staffId = staffId;
        this.name = name;
    }

	

    public int getStaffId() {
        return staffId;
    }

	

    public void setStaffId(int staffId) {
        this.staffId = staffId;
    }

	

    public String getName() {
        return name;
    }

	

    public void setName(String name) {
        this.name = name;
    }


}
